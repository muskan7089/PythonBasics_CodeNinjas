#Lists
SENSEI = ["Esohe", "Matt", "Beau", "Ray"]
print("Printing List data")
print(SENSEI)

print(len(SENSEI))

SENSEI.append("Zep")

print(SENSEI)

print(len(SENSEI))

#Tuples (fixed)
fruits = ("Apple", "Mango", "Peach", "Banananana")
print("Printing Tuple data")

print(fruits)

print(len(fruits))

# Set (Duplicates removed)
set = {"Cat", "Dog", "Duck", "Goose", "Dog"}
